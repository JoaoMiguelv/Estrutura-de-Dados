print(25*'-')

for i in range(5, 15): #Com dois argumentos o range gera uma lista do primeiro argumento (INCLUI) até o valor do último argumento (EXCLUI)
    print(i)

print(25*'-')

# range() com três argumentos:
# 1°: limite inferior (INCLUI)
# 2°: limite superior (EXCLUI)
# 3°: intervalo entre os elementos (Pode ser negativo)

for i in range(0, 20, 2):
    print(i)

print(25*'-')

for i in range(10, -1, -1):
    print(i)


    
    
    
    
    
    
    
    
    
    
    
    
    
    